    private DefaultListModel modelListPiloto(ArrayList<Piloto> pilotos) throws SQLException{
        DefaultListModel model = new DefaultListModel<>();
            for(Piloto p1 : pilotos){
                model.addElement(p1);
            }
    return model;
    }
    
    private void mostrarDetallesPiloto(Piloto piloto) {
        // Muestra los detalles del piloto en los campos de texto u otros componentes
        txtNif.setText(piloto.getNif());
        txtNombre.setText(piloto.getNombre());
        txtFecha.setText(""+piloto.getFechaNacimiento());
        txtSueldo.setText(String.valueOf(piloto.getSueldo()));
     
        String rutaFoto = piloto.getFoto();
        ImageIcon imagen = new ImageIcon(rutaFoto);
        Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(145, 181, Image.SCALE_DEFAULT));
        lblFoto.setIcon(icono);
        this.repaint();
    }
    
   private void mostrarImagen(File file) {
        ImageIcon icon = new ImageIcon(file.getAbsolutePath());
        Image image = icon.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_SMOOTH);
        lblFoto.setIcon(new ImageIcon(image));
    } 
