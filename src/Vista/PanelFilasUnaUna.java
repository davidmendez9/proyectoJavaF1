    private void btnPatrocinadoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPatrocinadoresActionPerformed
        int index = jList.getSelectedIndex();
        Piloto p1 = pilotos.get(index);
        
        try {
            panelJtable = new PanelJtable(JframePrincipal, p1);
        } catch (SQLException ex) {
            Logger.getLogger(PanelFilasUnaUna.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JframePrincipal.cambiarPanel(panelJtable);
        
        
        panelJtable.getLblPiloto().setText("Patrocinadores de "+p1.getNombre());
    }//GEN-LAST:event_btnPatrocinadoresActionPerformed

    private void btnSeleccionarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarImagenActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter("Archivos de imagen", "jpg", "jpeg", "png", "gif"));

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            try {
                // Obtener la nueva ruta de la imagen
                String nuevaRuta = selectedFile.getAbsolutePath();

                // Actualizar la interfaz de usuario con la nueva imagen
                mostrarImagen(selectedFile);
                int index = jList.getSelectedIndex();
        
                Piloto p1 = pilotos.get(index);
                p1.setFoto(nuevaRuta);

                // Actualizar la ruta de la imagen en la base de datos
                String nifPilotoSeleccionado = pilotos.get(jList.getSelectedIndex()).getNif();
                ConsultaEscuderia.actualizarImagenPiloto(nifPilotoSeleccionado, nuevaRuta);
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error al actualizar la imagen en la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSeleccionarImagenActionPerformed


